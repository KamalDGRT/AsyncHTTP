/// An enumeration with the possible HTTP Request Methods.
public enum HTTPMethod: String {
    case GET
    case POST
    case PUT
    case DELETE
    case PATCH
    case UPDATE
    case HEAD
    case TRACE
    case OPTIONS
    case CONNECT
    case SEARCH
    case COPY
    case MERGE
    case LABEL
    case LOCK
    case UNLOCK
    case MOVE
    case MKCOL
    case PROPFIND
    case PROPPATCH
}
